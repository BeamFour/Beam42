package org.redukti.jfotoptix.io;

public class Rgb {
    public final float r;
    public final float g;
    public final float b;
    public final float a;

    public Rgb (float red, float green, float blue, float alpha) {
        this.r = red;
        this.g = green;
        this.b = blue;
        this.a = alpha;
    }

}
